public class Main {

    public static void main(String[] args) {

        // This is a single-line comment — it will not be executed

        /*
         * This is a multi-line comment
         * also known as a block comment.
         * Hello my friends!
         * All these lines are ignored by the compiler.
         */

        System.out.println("Hello, World!"); // You can also add comments inline
    }
}